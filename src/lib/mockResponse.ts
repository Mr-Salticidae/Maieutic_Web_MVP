import type { ChatMessage, ChatResponse } from "./types";

export function createMockResponse(messages: ChatMessage[]): ChatResponse {
  const last = messages[messages.length - 1]?.content ?? "";

  if (/RAG|什么是|解释/.test(last)) {
    return {
      mode: "Knowledge",
      reply:
        "RAG 是 Retrieval-Augmented Generation，意思是先从外部资料中检索相关内容，再让模型基于这些内容回答。它解决的是模型只靠记忆容易过时或编造的问题。简单说，RAG 让 AI 回答前先翻资料。",
      insight: null,
      beacon: null
    };
  }

  if (/剪辑|学习|从哪里开始/.test(last)) {
    return {
      mode: "Exploration",
      reply:
        "先不急着列软件和课程。你说的学剪辑，可能是短视频节奏、故事叙事、商业广告、音乐卡点，或者个人影像表达。最先要分清的是：你想通过剪辑获得节奏爽感，还是想学会把一个故事讲清楚？",
      insight: null,
      beacon: "今晚找一个你喜欢的 30 秒视频，只标出 3 个让你觉得节奏舒服的剪切点。"
    };
  }

  if (/emo|难过|绝望|无望|抑郁|想哭|焦虑|压力大|崩溃/.test(last)) {
    return {
      mode: "Reflection",
      reply:
        "听到你这么说，一定是遇到了很大的困境。我们可以试着拆一拆：是什么具体的事情让你有这种感受？这件事背后，你真正在意的是什么？是 **能力不足**、**关系问题**，还是 **方向迷茫**？",
      insight: "情绪本身不是问题，问题是情绪背后的那个具体障碍。",
      beacon: "今晚花 10 分钟，只写下一件让你最难受的具体事，不需要分析，只描述事实。"
    };
  }

  if (/什么都想学|开始不了|迷茫|卡住/.test(last)) {
    return {
      mode: "Reflection",
      reply:
        "我先不急着给你时间管理方案。你说的什么都想学但什么都开始不了，可能不是懒，而是选择太多、兴奋过载、害怕选错，或者每个方向背后承载了不同期待。这里最需要分清的是：你现在更像太兴奋不知道先选哪个，还是其实很累所以任何一个都启动不了？",
      insight: null,
      beacon: null
    };
  }

  if (/公益|乡村|孩子|AI 项目|AI公益/.test(last)) {
    return {
      mode: "Creation",
      reply:
        "这个项目的第一问题不是做什么 AI 功能，而是谁能真正把它送到孩子面前。如果直接做 App，最可能卡在触达和持续使用上；更现实的入口可能是乡村老师、公益组织或已有阅读课程。你可以先把问题拆成三件事：孩子现在最缺什么、谁已经在接触他们、AI 能不能降低这个中间人的负担。",
      insight: "这个公益项目的关键不是把 AI 交给孩子，而是先找到能稳定触达孩子的人。",
      beacon: "今天先列出 3 类可能的中间触达者，并写下他们各自最可能缺的一项支持。"
    };
  }

  if (/到底应该|选哪个|学导演|编剧|AI 产品|替我决定/.test(last)) {
    return {
      mode: "Reflection",
      reply:
        "我不能替你选，因为这三个方向背后是三种不同的人生节奏和满足感来源。导演是视觉表达和现场掌控，编剧是文字结构和长线叙事，AI 产品是系统思考和快速迭代。比起问选哪个，更有价值的问题可能是：哪一件事是你即使不赚钱、没人认可，也愿意持续做三年的？或者反过来：哪个方向如果不试，你五年后会最遗憾？",
      insight: "职业选择难，往往不是信息不够，而是你还没分清想成为谁和想过上什么样的生活。",
      beacon: "今天花 20 分钟，给每个方向写一句话：如果选了它，最坏的情况是什么？我能不能接受？"
    };
  }

  if (/案例|参考|学习类|AI Agent|Skill|迭代|找几个/.test(last)) {
    return {
      mode: "Research",
      reply:
        "我先说明一个边界：当前我没有联网搜索能力，所以不能保证以下案例是最新或最全的。但基于已有知识，有几个方向的产品形态值得你参考：一类是引导式学习如 Khanmigo，它不是直接给答案而是用追问推进；一类是记忆型共学如 Mem，它帮你沉淀和连接读过的内容；还有一类是创作伙伴如 NotebookLM，它基于你给的素材做对话和整理。回到 Maieutic 的迭代，真正的问题不是还有谁在做类似的事，而是 Maieutic 最不可替代的那一下是什么——是模式判断的精准度、Insight 的命名能力，还是 Beacon 的可执行性？",
      insight: "参考案例最容易犯的错是抄功能而不是抄底层判断。",
      beacon: "今晚把你觉得 Maieutic 最不一样的那一点，用一句话写下来，不超过 20 字。"
    };
  }

  if (/写歌|suno|作曲|歌词|音乐|创作|拍短片|剧本|小说|写诗|文案/.test(last)) {
    return {
      mode: "Creation",
      reply:
        "这个创作方向很有力量。你已经有了表达的冲动和核心感受——被现实折磨的年轻女孩、重男轻女、资源不公、被物化。现在需要把这些感受落地成具体的创作元素：比如一首歌的话，可以先确定一个核心意象（镜子？牢笼？影子？），一段重复的副歌节奏，或者一个能代表困境的动作。先不急着写完整首，从一个小片段开始。",
      insight: "创作的起点不是完整的作品，而是一个能抓住人的小钩子。",
      beacon: "今晚选一个你觉得最能代表这个主题的具体意象（比如镜子、牢笼、影子），写 3 句和它相关的歌词或句子。"
    };
  }

  return {
    mode: "Reflection",
    reply:
      "我听到了你想说的，但需要更多细节才能帮你把问题理清楚。你可以试着描述一下：这个想法最打动你的是什么？你希望通过它达到什么？或者你现在卡在了哪个环节？",
    insight: null,
    beacon: "花 5 分钟，写下这个问题背后让你最在意的那件具体的事。"
  };
}
